def api_search(q, lim=10):
    """FTS5搜索——分词后多关键词取并集"""
    results = []
    seen = set()
    # 将问题拆成2-4字中文关键词逐个搜索
    keywords = re.findall(r'[一-鿿]{2,4}|[a-zA-Z]+|\d+', q)
    for kw in keywords[:5]:  # 最多5个关键词
        r = fts_search(kw, limit=lim//2)
        for item in r:
            key = item[2][:100]
            if key not in seen:
                seen.add(key)
                results.append(item)
    return results[:lim]
