cats = {"1":[],"2":[],"3":[],"4":[]}
for qt, ans, cat in QA_PAIRS:
    # 搜问题关键词
    ctx = []
    for kw in re.findall(r'[一-鿿]{2,4}|[a-zA-Z]+|\d+', qt):
        if kw not in STOP:
            r = api_search(kw, 3)
            ctx.extend(r)
    # 去重上下文
    seen = set(); ctx_unique = []
    for c in ctx:
        key = c[2][:100] if len(c) > 2 else str(c)[:100]
        if key not in seen: seen.add(key); ctx_unique.append(c)
    ct = ' '.join(c[2][:300] for c in ctx_unique[:10])
    # n-gram分词——和搜索保持一致
    ans_words = []
    for w in re.findall(r'[a-zA-Z]+|\d+', ans):
        if w not in STOP: ans_words.append(w)
    cn = ''.join(re.findall(r'[\u4e00-\u9fff]', ans))
    for n in (2, 3, 4):
        for i in range(len(cn) - n + 1):
            token = cn[i:i+n]
            if token not in STOP: ans_words.append(token)
    match = sum(1 for w in ans_words if w in ct)
    score = 3 if match >= 3 else (2 if match >= 2 else 1)
    if score >= 3: hits += 1

    cats[str(cat)] = cats.get(str(cat), []) + [score]
    total += 1

print(f"\n中文LoCoMo: {total}题 {hits}命中 = {hits/total*100:.0f}%")
